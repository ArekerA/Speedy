package dane;

import java.util.Date;

/**
 *
 * @author Arekl
 */
public class Zlecenie {
    int id;
    Date data;

    public Zlecenie(int id, Date data) {
        this.id = id;
        this.data = data;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }
    
}
